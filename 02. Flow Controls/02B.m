% Practice 2)

% Define the vector X
X = [1 2 3 4 5 6];

% Add up all elements of the vector
% sum() computes the total of all values in x
A = sum(X);

% Compute the sine of each x-value
% sin() operates element-wise and returns a vector
B = sin(X);

% Display results
disp('Sum of all elements in X:');
disp(A);

disp('Sine of each element in X:');
disp(B);
